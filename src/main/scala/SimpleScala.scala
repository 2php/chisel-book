class SimpleScala {

  /*
  //- start scala_val
  // A value is a constant
  val zero = 0
  // No new assignment; this will not compile
  zero = 3
  //- end
  */

  //- start scala_var
  // We can change the value os a var variable
  var x = 2
  x = 3
  //- end

  //- start scala_int_type
  val number: Int = 42
  //- end

  var v = "Hello"
  v = "Hello World"

  // Type usually inferred, but can be declared
  var s: String = "abc"

}
